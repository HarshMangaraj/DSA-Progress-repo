#comparision values for time complexity

#n represents the number of elements in the input data structure. The time complexity of an algorithm is a measure of how the running time or space requirements grow as the input size grows. or in simple words the size of the input data structure.

# 1 < log log n < log n < n < n^2 < n^3 < n^4 < ...... < 2^n

# 1 is the best time complexity and 2^n is the worst time complexity.

# 1 is the smallest then log log n, then log n, then n, then n^2, then n^3, then n^4 and so on and 2^n is the largest time complexity.